def test_gpcrmd_manifest_requires_existing_paths_for_required_inputs(tmp_path: Path):
    target = default_gpcrmd_targets()[0]
    model = next(item for item in target.files if item.role == "model")
    topology = next(item for item in target.files if item.role == "topology")
    parameters = next(item for item in target.files if item.role == "parameters")
    protocol = next(item for item in target.files if item.role == "protocol")
    trajectory = next(item for item in target.files if item.role == "trajectory")
    protocol_path = tmp_path / "protocol.dat"
    trajectory_path = tmp_path / "missing-trajectory.dcd"
    protocol_path.write_text("fixture\n")
    manifest = tmp_path / "cache_manifest.json"
    manifest.write_text(
        "{\n"
        '  "files": [\n'
        f'    {{"file_id": {model.file_id}}},\n'
        f'    {{"file_id": {topology.file_id}, "path": "missing-topology.psf"}},\n'
        f'    {{"file_id": {parameters.file_id}, "path": "missing-parameters.prm"}},\n'
        f'    {{"file_id": {protocol.file_id}, "path": "{protocol_path.name}"}},\n'
        f'    {{"file_id": {trajectory.file_id}, "path": "{trajectory_path.name}"}}\n'
        "  ]\n"
        "}\n"
    )

    inspection = inspect_gpcrmd_cache(manifest, target_id=target.target_id)
    report = gpcrmd_mlx_compatibility_report(inspection)
    statuses = {status.role: status for status in inspection.file_statuses}

    assert inspection.complete is False
    assert statuses["model"].present is False
    assert statuses["model"].path is None
    assert statuses["topology"].present is False
    assert statuses["topology"].path == str(tmp_path / "missing-topology.psf")
    assert statuses["parameters"].present is False
    assert statuses["protocol"].present is True
    assert statuses["trajectory"].present is False
    assert f"file:model:{model.file_id}" in report.missing_input
    assert f"file:topology:{topology.file_id}" in report.missing_input
    assert f"file:parameters:{parameters.file_id}" in report.missing_input
    assert f"file:trajectory:{trajectory.file_id}" not in report.missing_input
    assert report.next_engine_slice == "download_or_mount_complete_gpcrmd_package_before_import"


