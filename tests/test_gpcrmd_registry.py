def _amber_text_values(values: list[str], *, per_line: int = 5, width: int = 16) -> str:
    lines = []
    for start in range(0, len(values), per_line):
        lines.append("".join(f"{value:>{width}}" for value in values[start : start + per_line]))
    return "\n".join(lines) + "\n"


def _amber_atom_names(names: list[str]) -> str:
    return "\n".join(
        "".join(f"{name:<4}" for name in names[start : start + 20])
        for start in range(0, len(names), 20)
    ) + "\n"


def _write_tiny_amber_prmtop(path: Path) -> None:
    atom_names = ["C1", "H1", "O1", "H2", "O", "H3", "H4", "NA", "CL", "C2", "H5"]
    residues = ["ALA", "LIG", "WAT", "NA", "CL", "POPC"]
    residue_pointers = [1, 3, 5, 8, 9, 10]
    pointers = ["0"] * 28
    pointers[0] = "11"
    pointers[1] = "1"
    pointers[27] = "1"
    bond_records = [
        (0, 3, 1),
        (6, 9, 1),
        (12, 15, 1),
        (12, 18, 1),
        (27, 30, 1),
    ]
    path.write_text(
        "%VERSION  VERSION_STAMP = V0001.000\n"
        "%FLAG POINTERS\n"
        "%FORMAT(10I8)\n"
        f"{_amber_text_values(pointers, per_line=10, width=8)}"
        "%FLAG ATOM_NAME\n"
        "%FORMAT(20a4)\n"
        f"{_amber_atom_names(atom_names)}"
        "%FLAG CHARGE\n"
        "%FORMAT(5E16.8)\n"
        f"{_amber_text_values(['0.00000000E+00'] * len(atom_names))}"
        "%FLAG MASS\n"
        "%FORMAT(5E16.8)\n"
        + _amber_text_values(
            [
                "1.20000000E+01",
                "1.00800000E+00",
                "1.59990000E+01",
                "1.00800000E+00",
                "1.59990000E+01",
                "1.00800000E+00",
                "1.00800000E+00",
                "2.29900000E+01",
                "3.54500000E+01",
                "1.20000000E+01",
                "1.00800000E+00",
            ]
        )
        + "%FLAG ATOM_TYPE_INDEX\n"
        "%FORMAT(10I8)\n"
        f"{_amber_text_values(['1'] * len(atom_names), per_line=10, width=8)}"
        "%FLAG AMBER_ATOM_TYPE\n"
        "%FORMAT(20a4)\n"
        f"{_amber_atom_names(['C', 'H', 'O', 'H', 'O', 'H', 'H', 'NA', 'CL', 'C', 'H'])}"
        "%FLAG RESIDUE_LABEL\n"
        "%FORMAT(20a4)\n"
        f"{_amber_atom_names(residues)}"
        "%FLAG RESIDUE_POINTER\n"
        "%FORMAT(10I8)\n"
        f"{_amber_text_values([str(item) for item in residue_pointers], per_line=10, width=8)}"
        "%FLAG NONBONDED_PARM_INDEX\n"
        "%FORMAT(10I8)\n"
        f"{_amber_text_values(['1'], per_line=10, width=8)}"
        "%FLAG LENNARD_JONES_ACOEF\n"
        "%FORMAT(5E16.8)\n"
        "  4.00000000E+00\n"
        "%FLAG LENNARD_JONES_BCOEF\n"
        "%FORMAT(5E16.8)\n"
        "  4.00000000E+00\n"
        "%FLAG BONDS_INC_HYDROGEN\n"
        "%FORMAT(10I8)\n"
        + _amber_text_values(
            [str(value) for record in bond_records for value in record],
            per_line=10,
            width=8,
        )
        + "%FLAG BOND_FORCE_CONSTANT\n"
        "%FORMAT(5E16.8)\n"
        "  1.00000000E+02\n"
        "%FLAG BOND_EQUIL_VALUE\n"
        "%FORMAT(5E16.8)\n"
        "  1.00000000E+00\n"
    )


